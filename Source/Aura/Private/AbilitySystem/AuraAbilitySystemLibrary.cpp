void UAuraAbilitySystemLibrary::GetLivePlayersWithinRadius(const UObject* WorldContextObject, TArray<AActor*>& OutOverlappingActors,
	const TArray<AActor*>& ActorsToIgnore, float Radius, const FVector& SphereOrigin)
{
	FCollisionQueryParams SphereParams;
	SphereParams.AddIgnoredActors(ActorsToIgnore);

	TArray<FOverlapResult> Overlaps;
	if (const UWorld* World = GEngine->GetWorldFromContextObject(WorldContextObject, EGetWorldErrorMode::LogAndReturnNull))
	{
		World->OverlapMultiByObjectType(Overlaps, SphereOrigin, FQuat::Identity, FCollisionObjectQueryParams(FCollisionObjectQueryParams::InitType::AllDynamicObjects), FCollisionShape::MakeSphere(Radius), SphereParams);
		for (FOverlapResult& Overlap : Overlaps)
		{
			if (Overlap.GetActor()->Implements<UCombatInterface>() && !ICombatInterface::Execute_IsDead(Overlap.GetActor())) // Checks if Actor has the interface, if it has not, check after the '&&' will not work so it will not crash
			{
				OutOverlappingActors.AddUnique(ICombatInterface::Execute_GetAvatar(Overlap.GetActor()));
			}
		}
	}
}
void UAuraAbilitySystemLibrary::GetClosestTargets(int32 MaxTargets, const TArray<AActor*>& Actors, TArray<AActor*>& OutClosestTargets, const FVector& Origin)
{

	if (MaxTargets < 1)
	{
		OutClosestTargets.Empty();
		return;
	}

	OutClosestTargets = Actors;
	Algo::Sort(OutClosestTargets, [&Origin](AActor* A, AActor* B)
	{
		// Calculating the squared distance to avoid expensive Sqrt() call.
		const float DistanceA = FVector::DistSquared(A->GetActorLocation(), Origin);
		const float DistanceB = FVector::DistSquared(B->GetActorLocation(), Origin);
		return DistanceA < DistanceB;
	});
	if (OutClosestTargets.Num() > MaxTargets)
	{
		OutClosestTargets.RemoveAt(MaxTargets, OutClosestTargets.Num() - MaxTargets);
	}
}

bool UAuraAbilitySystemLibrary::IsNotFriend(AActor* FirstActor, AActor* SecondActor)
{
	const bool bBothArePlayers = FirstActor->ActorHasTag(FName("Player")) && SecondActor->ActorHasTag(FName("Player"));
	const bool bBothAreEnemies = FirstActor->ActorHasTag(FName("Enemy")) && SecondActor->ActorHasTag(FName("Enemy"));
	
	const bool bFriends = bBothAreEnemies || bBothArePlayers;
	return !bFriends;
}


FGameplayEffectContextHandle UAuraAbilitySystemLibrary::ApplyDamageEffect(const FDamageEffectParams& DamageEffectParams)
{
	// Validate your source ASC
	UAbilitySystemComponent* SourceASC = DamageEffectParams.SourceAbilitySystemComponent;
	if (!SourceASC)
	{
		return FGameplayEffectContextHandle();
	}

	TSubclassOf<UGameplayEffect> DamageGEClass = DamageEffectParams.DamageGameplayEffectClass;

	// Build the context safely
	FGameplayEffectContextHandle EffectContext = SourceASC->MakeEffectContext();
	if (AActor* SourceAvatar = SourceASC->GetAvatarActor())
	{
		EffectContext.AddSourceObject(SourceAvatar);
	}

	// Make the outgoing spec
	FGameplayEffectSpecHandle SpecHandle = SourceASC->MakeOutgoingSpec(
		DamageGEClass,
		DamageEffectParams.AbilityLevel,
		EffectContext
	);

	if (!SpecHandle.IsValid() || !SpecHandle.Data.IsValid())
	{
		return EffectContext;
	}

	// Assign your SetByCaller magnitudes
	const FAuraGameplayTags& GameplayTags = FAuraGameplayTags::Get();
	UAbilitySystemBlueprintLibrary::AssignTagSetByCallerMagnitude(SpecHandle, DamageEffectParams.DamageType, DamageEffectParams.BaseDamage);
	UAbilitySystemBlueprintLibrary::AssignTagSetByCallerMagnitude(SpecHandle, GameplayTags.Debuff_Chance, DamageEffectParams.DebuffChance);
	UAbilitySystemBlueprintLibrary::AssignTagSetByCallerMagnitude(SpecHandle, GameplayTags.Debuff_Damage, DamageEffectParams.DebuffDamage);
	UAbilitySystemBlueprintLibrary::AssignTagSetByCallerMagnitude(SpecHandle, GameplayTags.Debuff_Duration, DamageEffectParams.DebuffDuration);
	UAbilitySystemBlueprintLibrary::AssignTagSetByCallerMagnitude(SpecHandle, GameplayTags.Debuff_Frequency, DamageEffectParams.DebuffFrequency);

	// Pack impulses
	SetDeathImpulse(EffectContext, DamageEffectParams.DeathImpulse);
	SetKnockbackForce(EffectContext, DamageEffectParams.KnockbackForce);

	// Radial Damage
	SetIsRadialDamage(EffectContext, DamageEffectParams.bIsRadialDamage);
	SetRadialDamageInnerRadius(EffectContext, DamageEffectParams.RadialDamageInnerRadius);
	SetRadialDamageOuterRadius(EffectContext, DamageEffectParams.RadialDamageOuterRadius);
	SetRadialDamageOrigin(EffectContext, DamageEffectParams.RadialDamageOrigin);

	// Apply to the target ASC if valid
	UAbilitySystemComponent* TargetASC = DamageEffectParams.TargetAbilitySystemComponent;
	if (TargetASC)
	{
		TargetASC->ApplyGameplayEffectSpecToSelf(*SpecHandle.Data.Get());
	}
	
	return EffectContext;
}

TArray<FRotator> UAuraAbilitySystemLibrary::EvenlySpacedRotators(const FVector& Forward, const FVector& Axis, float Spread, int32 NumRotators)
{
	TArray<FRotator> Rotators;

	const FVector LeftOfSpread = Forward.RotateAngleAxis(-Spread / 2.f, Axis);
	if (NumRotators > 1)
	{
		const float DeltaSpread = Spread / (NumRotators - 1);
		for (int32 i = 0; i < NumRotators; i++)
		{
			const FVector Direction = LeftOfSpread.RotateAngleAxis(DeltaSpread * i, Axis);
			Rotators.Add(Direction.Rotation());
		}
	}
	else
	{
		Rotators.Add(Forward.Rotation());
	}

	return Rotators;
}

TArray<FVector> UAuraAbilitySystemLibrary::EvenlyRotatedVectors(const FVector& Forward, const FVector& Axis, float Spread, int32 NumVectors)
{
	TArray<FVector> Vectors;

	const FVector LeftOfSpread = Forward.RotateAngleAxis(-Spread / 2.f, Axis);
	if (NumVectors > 1)
	{
		const float DeltaSpread = Spread / (NumVectors - 1);
		for (int32 i = 0; i < NumVectors; i++)
		{
			const FVector Direction = LeftOfSpread.RotateAngleAxis(DeltaSpread * i, Axis);
			Vectors.Add(Direction);
		}
	}
	else
	{
		Vectors.Add(Forward);
	}

	return Vectors;
}

